EnemyBase::EnemyBase()
{
	isAttackHit = false;
	isHit = false;
}

//---------------------------------------------------------------------------
//! エネミーの座標
//---------------------------------------------------------------------------
void EnemyBase::RandPos()
{
	pos.x = (float)GetRand(1100) - 550; //!< エネミーのX座標(-550〜550のランダム)
	pos.y = ENEMY_Y;                   //!< エネミーのY座標(110に固定)
	pos.z = (float)GetRand(1100) - 550; //!< エネミーのZ座標(-550〜550のランダム)

	EnemyMat.m[3][0] = pos.x;
	EnemyMat.m[3][1] = pos.y;
	EnemyMat.m[3][2] = pos.z;
}
